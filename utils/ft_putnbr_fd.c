#include"libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		ft_putnbr_fd(147483648, fd);
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n * -1, fd);
	}
	else if (n <= 9)
		ft_putchar_fd(n + 48, fd);
	else
	{
		c = (n % 10) + 48;
		n = n / 10;
		ft_putnbr_fd(n, fd);
		ft_putchar_fd(c, fd);
	}
}
