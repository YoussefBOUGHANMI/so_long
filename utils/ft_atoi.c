#include"libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		signe;
	long	nbr;

	i = 0;
	nbr = 0;
	signe = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (int)(str[i] - 48);
		i++;
	}
	nbr = signe * nbr;
	return (nbr);
}
